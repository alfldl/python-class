# 수치형 자료를 문자열로 변환하기 ( str() 함수 )
num1 = 1234
num2 = 3.14

numstr1 = str(num1)         # 정수를 문자열로 변환
numstr2 = str(num2)         # 실수를 문자열로 변환
print(type(numstr1))
print(type(numstr2))
print('num1을 문자열로 변환한 값은 “%s” 입니다.' %numstr1)
print('num2를 문자열로 변환한 값은 “%s” 입니다.' %numstr2)

print('num1을 문자열로 변환한 값은 “%d” 입니다.' %num1)

