# 비교(관계) 연산자

x = 1
y = 2
str1 = 'abc'
str2 = 'python'

print(" x == y ->", x == y)                # False가 출력됨
print("  x != y ->", x != y)                # True가 출력됨
print(" str1 == str2 ->", str1 == str2)          # False가 출력됨
print(" str2 == 'python' ->", str2 == 'python')     # True가 출력됨

print("str1 < str2 ->", str1 < str2)           # True가 출력됨

# 문자열의 크기 비교는 문자열의 사전 순서로 비교합니다.
# abc 가  python 보다 사전 순서가 앞이므로 결과는 True 가 출력
